public class QueryEntity {
    private String id;
    private String feature;

    public QueryEntity(String id, String feature) {
        this.id = id;
        this.feature = feature;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getFeature() {
        return feature;
    }

    public void setFeature(String feature) {
        this.feature = feature;
    }
}
